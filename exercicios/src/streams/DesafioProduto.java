package streams;

public class DesafioProduto {
	
	final String nome;
	final double preco;
	final int quantidade;
	final String fornecedor;
	
	
	
	public DesafioProduto(String nome, double preco, int quantidade, String fornecedor) {
		
		this.nome = nome;
		this.preco = preco;
		this.quantidade = quantidade;
		this.fornecedor = fornecedor;
	}
	
	
	
	

}
